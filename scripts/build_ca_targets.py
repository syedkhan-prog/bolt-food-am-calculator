def pct(s) -> float:
    if s is None or s == "":
        return 0.0
    t = str(s).strip().replace(",", "")
    if t.endswith("%"):
        return float(t[:-1]) / 100.0
    try:
        return float(t)
    except ValueError:
        return 0.0


def gmv_val(s) -> float:
    if not s:
        return 0.0
    t = re.sub(r"[^\d.]", "", str(s))
    return float(t) if t else 0.0


def row_to_country(row: list) -> dict | None:
    if not row or not row[0]:
        return None
    name = str(row[0]).strip()
    cc = NAME_TO_CODE.get(name)
    if not cc:
        return None
    # Global Summary layout (cols E=4 AM, F=5 Marketing, M=12 Bolt+, R/S/T=17/18/19, AC=28, AL=35)
    di = {
        "new_city": pct(row[1]) if len(row) > 1 else 0,
        "elc": pct(row[2]) if len(row) > 2 else 0,
        "churn_untested": pct(row[3]) if len(row) > 3 else 0,
        "am_food": pct(row[4]) if len(row) > 4 else 0,
        "marketing_food": pct(row[5]) if len(row) > 5 else 0,
        "bdm_3p": pct(row[6]) if len(row) > 6 else 0,
        "store_1p": pct(row[7]) if len(row) > 7 else 0,
        "untested_total": pct(row[9]) if len(row) > 9 else 0,
        "bolt_plus": pct(row[12]) if len(row) > 12 else 0,
        "activation": pct(row[17]) if len(row) > 17 else 0,
        "churned": pct(row[18]) if len(row) > 18 else 0,
        "occasional": pct(row[19]) if len(row) > 19 else 0,
        "total_bolt": pct(row[28]) if len(row) > 28 else 0,
        "total_campaign": pct(row[33]) if len(row) > 33 else 0,
    }
    target_gmv = gmv_val(row[35]) if len(row) > 35 else 0
    return {
        "country_name": name,
        "gmv_target": target_gmv,
        "di_pct": di,
    }


def build_month(month: str, sheet_id: str, label: str, rows: list, carry_from: dict | None = None) -> dict:
    countries = {}
    for row in rows:
        c = row_to_country(row)
        if c and c["gmv_target"] > 0:
            countries[NAME_TO_CODE[c["country_name"]]] = c

    if carry_from:
        for cc, data in carry_from.get("countries", {}).items():
            if cc in countries:
                continue
            if data.get("gmv_target", 0) <= 0:
                continue
            countries[cc] = {**data, "note": "DI% carried from prior month sheet"}

    return {
        "month": month,
        "sheet_id": sheet_id,
        "label": label,
        "refreshed": "2026-06-28",
        "countries": countries,
    }


